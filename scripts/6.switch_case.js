// SWITCH - CASE 

const opcao = 3

switch (opcao) {
    case 1:
        console.log ("Opção 1") 
        break;
    case 2:
        console.log ("Opção 2") 
        break;
    case 3:
        console.log ("Opção 3")
        break;

    case 4:
        console.log ("Opção 4")
        break;

    case 50:
        console.log ("Opção 5")
        break;
    default:
        console.log ("Opção inválida")
        break;
}